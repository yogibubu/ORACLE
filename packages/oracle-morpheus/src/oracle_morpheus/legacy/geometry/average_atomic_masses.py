# ---------------------------------------------------------------------
# Public API
# ---------------------------------------------------------------------

def atomic_mass(Z: int) -> float:
    """
    Return mean atomic mass for atomic number Z.
    Faithful to Fortran FilAMS.
    """
    if Z <= 0:
        return 0.0

    if Z > _MAX_AN:
        raise ValueError("Atomic number out of range in atomic_mass")

    return _ATOMIC_MASS.get(Z, 0.0)
